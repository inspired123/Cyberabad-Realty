                    <li>Fire</li>
                    <li>Storm, tempest, flood &amp; inundation</li>
                    <li>Riot, strike &amp; malicious damage</li>
                    <li>Lightning</li>
                    <li>Explosion &amp; implosion</li>
                    <li>Aircraft damage</li>
                    <li>Damage due to impact by vehicles</li>
