                    <li>Leakage from automatic Sprinkler installations</li>
                    <li>Bush fire</li>
                </ul>
            </li>
            <li><b>Earthquake Cover</b><br>
                Covers damages to the structure of your house due to earthquake.</li>
            <li><b>Terrorism Cover</b><br>
                Covers damages to the structure of your house due to acts of terrorism.</li>
        </ul>
        
        A home insurance does not cover the market value of the home. The price of the 
        home includes the cost of the land and the cost of constructing the building 
        structure on this land and the land cannot be insured. The insurance cover is 
        only for the cost of constructing the building. The sum insured is calculated by 
        multiplying your home area by the construction rate per sq. feet.<br>
        <br>
        
        <br>
        <b>Insurance Cover for Contents Inside the Home</b><br>
        This cover is only for damages or loss of the contents inside the home 
        -electronic and electrical goods, furniture and fixtures, clothing, jewelry and 
        any other contents inside the home. The covers that can be taken for the 
        contents are as follows:
        <ul>
            <li>The Fire and Specials Perils Cover</li>
            <li>Earthquake Cover</li>
